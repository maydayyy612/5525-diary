const MAP_BOUNDS = { lonMin: 73, lonMax: 135, latMin: 18, latMax: 53 };
const MAP_VIEW = { w: 860, h: 630 };

function project(lon, lat) {
  const x = (lon - MAP_BOUNDS.lonMin) / (MAP_BOUNDS.lonMax - MAP_BOUNDS.lonMin) * MAP_VIEW.w;
  const y = (MAP_BOUNDS.latMax - lat) / (MAP_BOUNDS.latMax - MAP_BOUNDS.latMin) * (MAP_VIEW.h - 30);
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

