function getCityCoord(cityName) {
  if (!cityName) return null;
  const key = String(cityName).trim();
  if (CITY_COORDS[key]) return CITY_COORDS[key];
  // 容错：去掉"市"字后再试一次，例如"北京市" -> "北京"
  const stripped = key.replace(/市$/, "");
  if (CITY_COORDS[stripped]) return CITY_COORDS[stripped];
  return null;
}

function distanceFromBeijing(cityName) {
  const coord = getCityCoord(cityName);
  if (!coord) return null;
  return haversineKm(BEIJING_COORD.lat, BEIJING_COORD.lon, coord[1], coord[0]);
}
